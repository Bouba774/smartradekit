/**
 * Obtenir la configuration d'un actif
 * @throws Error si l'actif n'est pas configuré
 */
export function getAssetConfig(symbol: string): AssetConfig {
  const config = ASSET_CONFIGS[symbol];
  if (!config) {
    throw new Error(`Configuration manquante pour l'actif: ${symbol}. Veuillez contacter le support.`);
  }
  return config;
}

/**
 * Vérifier si un actif est supporté
 */
export function isAssetSupported(symbol: string): boolean {
  return symbol in ASSET_CONFIGS;
}

/**
 * Obtenir tous les symboles supportés
 */
export function getAllSupportedSymbols(): string[] {
  return Object.keys(ASSET_CONFIGS);
}

/**
 * Obtenir les symboles par type d'actif
 */
export function getSymbolsByType(assetType: AssetType): string[] {
  return Object.values(ASSET_CONFIGS)
    .filter(config => config.assetType === assetType)
    .map(config => config.symbol);
}
