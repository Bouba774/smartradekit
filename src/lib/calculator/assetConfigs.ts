export type AssetType = 
  | 'forex_non_jpy'
  | 'forex_jpy'
  | 'metal_gold'
  | 'metal_silver'
  | 'metal_platinum'
  | 'index_us'
  | 'index_eu'
  | 'index_asia'
  | 'crypto_major'
  | 'crypto_alt'
  | 'energy'
  | 'stock';

export type RoundingMode = 'DOWN' | 'HALF_UP';

export interface AssetConfig {
  symbol: string;
  assetType: AssetType;
  priceDecimals: number;        // Nombre de décimales du prix (ex: 5 pour EURUSD)
  pipSize: number;              // Taille d'un pip (ex: 0.0001 pour EURUSD)
  tickSize: number;             // Plus petite variation de prix
  contractSize: number;         // Taille du contrat pour 1 lot standard
  minLot: number;               // Lot minimum tradable
  maxLot: number;               // Lot maximum tradable
  lotStep: number;              // Incrément de lot
  quoteCurrency: string;        // Devise de cotation
  baseCurrency?: string;        // Devise de base (pour forex)
  roundingMode: RoundingMode;   // Mode d'arrondi pour les lots
}

